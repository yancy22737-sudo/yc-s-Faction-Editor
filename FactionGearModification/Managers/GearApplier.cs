        public static void ApplyCustomGear(Pawn pawn, Faction faction)
        {
            try
            {
                if (pawn == null || faction == null) return;

                FactionGearData factionData = null;
                FactionGearData globalFallbackData = null;
                if (PreviewPreset != null)
                {
                    // 预览模式：使用预览预设
                    factionData = PreviewPreset.factionGearData?.FirstOrDefault(f => f.factionDefName == faction.def?.defName);
                }
                else
                {
                    // 正常模式：优先使用存档设置，否则使用全局设置
                    var gameComponent = FactionGearGameComponent.Instance;
                    var saveData = gameComponent?.GetActiveFactionGearData();
                    globalFallbackData = FactionGearCustomizerMod.Settings?.TryGetFactionData(faction.def?.defName);

                    if (saveData != null)
                    {
                        // 使用存档中的设置；如果存档内不存在该派系，则回退到全局设置
                        factionData = saveData.FirstOrDefault(f => f.factionDefName == faction.def?.defName);
                        if (factionData == null)
                        {
                            factionData = globalFallbackData;
                        }
                    }
                    else
                    {
                        // 使用全局设置（兼容旧存档或主菜单）
                        factionData = globalFallbackData;
                    }
                }

                if (factionData == null) return;

                var kindDefName = pawn.kindDef?.defName;

                if (!string.IsNullOrEmpty(kindDefName))
                {
                    var kindData = factionData.GetKindData(kindDefName);

                    // 存档数据优先；若当前派系数据来自存档且该 kind 未修改，则允许回退到全局同派系配置。
                    if (!ShouldApplyKindData(kindData) &&
                        globalFallbackData != null &&
                        !ReferenceEquals(globalFallbackData, factionData))
                    {
                        var fallbackKindData = globalFallbackData.GetKindData(kindDefName);
                        if (ShouldApplyKindData(fallbackKindData))
                        {
                            kindData = fallbackKindData;
                        }
                    }

                    if (ShouldApplyKindData(kindData))
                    {
                        // Apply inventory first so ForceOnlySelected clearing does not remove runtime ammo generated by weapons.
                        ApplyInventory(pawn, kindData);
                        ApplyWeapons(pawn, kindData);
                        ApplyApparel(pawn, kindData);
                        ApplyHediffs(pawn, kindData);
                        // Log.Message($"[FactionGearCustomizer] Successfully applied custom gear to pawn {pawn.Name.ToStringFull} ({pawn.kindDef.defName}) of faction {faction.Name}");
                    }
                }
            }
            catch (Exception ex)
            {
                Log.Warning($"[FactionGearCustomizer] Error in ApplyCustomGear for pawn {pawn?.Name?.ToStringFull ?? "Unknown"}: {ex}");
            }
        }

        private static bool ShouldApplyKindData(KindGearData kindData)
        {
            // Only apply runtime gear patches for explicitly modified kind configs.
            // Unmodified entries may exist due to UI browsing and must not alter vanilla loadouts.
            return kindData != null && kindData.isModified;
        }

        /// <summary>
        /// 检查服装是否适合 pawn 穿戴（包括身体部位和年龄限制）
        /// </summary>
        private static bool CanWearApparel(Pawn pawn, ThingDef apparelDef)
        {
            if (pawn == null || apparelDef == null || !apparelDef.IsApparel)
                return false;

            // 检查身体部位
            if (!ApparelUtility.HasPartsToWear(pawn, apparelDef))
                return false;

            // 检查年龄限制（儿童/成人服装限制）
            if (!apparelDef.apparel.PawnCanWear(pawn))
                return false;

            return true;
        }

        /// <summary>
        /// 检查 Hediff 是否会导致 Pawn 意识或移动能力低于 50%，或者导致缺失身体部位
        /// 使用简化的检查方法：限制严重程度和类型
        /// </summary>
        private static bool WillHediffIncapacitatePawn(Pawn pawn, Hediff newHediff)
        {
            if (pawn == null || newHediff == null) return true;
            
            // 检查是否是危险类型的 Hediff
            if (newHediff.def != null)
            {
                // 跳过会导致缺失身体部位的 Hediff
                if (newHediff.def.hediffClass == typeof(Hediff_MissingPart))
                {
                    Log.Warning($"[FactionGearCustomizer] Skipped applying hediff {newHediff.def.defName} - MissingBodyPart hediffs are not allowed");
                    return true;
                }
                
                // 跳过会导致严重伤害的 Hediff（如致命伤、烧伤过重等）
                if (newHediff.def.defName.ToLower().Contains("tendable"))
                {
                    return true;
                }
                
                // 跳过导致昏迷的 Hediff
                if (newHediff.def.stages != null)
                {
                    foreach (var stage in newHediff.def.stages)
                    {
                        if (stage?.capMods != null)
                        {
                            foreach (var cap in stage.capMods)
                            {
                                // 如果会导致意识或移动能力完全丧失
                                if (cap.capacity != null)
                                {
                                    string capName = cap.capacity.defName.ToLower();
                                    if ((capName == "consciousness" || capName == "moving") && cap.offset <= -0.5f)
                                    {
                                        return true;
                                    }
                                }
                            }
                        }
                    }
                }
            }
            
            // 临时添加 Hediff 来检查效果
            bool added = false;
            try
            {
                pawn.health.AddHediff(newHediff);
                added = true;
                
                // 检查是否会昏迷（Downed 状态）
                if (pawn.Downed)
                {
                    return true;
                }
                
                // 检查是否失去意识（Unconscious）
                if (!pawn.Awake())
                {
                    return true;
                }
                
                // 检查是否有缺失的身体部位（特别是头部）
                if (pawn.health.hediffSet.HasHediff(HediffDefOf.MissingBodyPart))
                {
                    // 检查是否有头部缺失
                    var allHediffs = pawn.health.hediffSet.hediffs;
                    foreach (var hediff in allHediffs)
                    {
                        if (hediff is Hediff_MissingPart missing && missing.Part != null && missing.Part.def == BodyPartDefOf.Head)
                        {
                            return true;
                        }
                    }
                }
                
                // 对于严重的负面 Hediff，限制其严重程度
                if (newHediff.def != null && newHediff.def.isBad)
                {
                    // 如果严重程度超过 0.5，认为太危险
                    if (newHediff.Severity > 0.5f)
                    {
                        return true;
                    }
                }
                
                return false;
            }
            finally
            {
                // 移除临时添加的 Hediff
                if (added && newHediff != null)
                {
                    pawn.health.RemoveHediff(newHediff);
                }
            }
        }

        /// <summary>
        /// 安全地销毁所有服装，跳过不可摧毁的物品
        /// </summary>
        private static void SafeDestroyAllApparel(Pawn pawn)
        {
            if (pawn?.apparel?.WornApparel == null) return;

            var apparelList = pawn.apparel.WornApparel.ToList();
            foreach (var apparel in apparelList)
            {
                if (apparel == null || apparel.def == null) continue;

                // 跳过不可摧毁的物品
                if (!apparel.def.destroyable)
                {
                    pawn.apparel.Remove(apparel);
                    continue;
                }

                pawn.apparel.Remove(apparel);
                apparel.Destroy();
            }
        }

        private static void ApplyInventory(Pawn pawn, KindGearData kindData)
        {
            if (kindData.InventoryItems.NullOrEmpty() && !kindData.ForceOnlySelected) return;

            if (kindData.ForceOnlySelected && pawn.inventory != null && pawn.inventory.innerContainer != null)
            {
                pawn.inventory.innerContainer.ClearAndDestroyContents();
            }

            if (kindData.InventoryItems.NullOrEmpty()) return;

            // 预先计算所有要生成的物品及其数量，避免多次调用导致数量叠加
            var itemsToGenerate = CalculateItemsToGenerate(kindData.InventoryItems);
            ApplyInventoryItems(pawn, kindData, itemsToGenerate);
        }

        private static List<(SpecRequirementEdit spec, ThingDef thingDef, int count)> CalculateItemsToGenerate(List<SpecRequirementEdit> inventoryItems)
        {
            var aggregated = new Dictionary<ThingDef, (SpecRequirementEdit spec, int count)>();

            foreach (var item in GetWhatToGive(inventoryItems, null))
            {
                if (item.Thing == null) continue;
                if (IsSpecialItem(item.Thing)) continue;

                int count = item.CountRange.RandomInRange;
                if (count <= 0) count = 1;

                if (aggregated.TryGetValue(item.Thing, out var existing))
                {
                    aggregated[item.Thing] = (existing.spec ?? item, existing.count + count);
                }
                else
                {
                    aggregated[item.Thing] = (item, count);
                }
            }

            return aggregated
                .Select(kvp => (kvp.Value.spec, kvp.Key, kvp.Value.count))
                .ToList();
        }

        private static void ApplyInventoryItems(Pawn pawn, KindGearData kindData, List<(SpecRequirementEdit spec, ThingDef thingDef, int count)> itemsToGenerate)
        {
            if (pawn == null || pawn.inventory == null || pawn.inventory.innerContainer == null) return;

            foreach (var (spec, thingDef, count) in itemsToGenerate)
            {
                int currentCount = pawn.inventory.innerContainer.Where(t => t.def == thingDef).Sum(t => t.stackCount);
                if (currentCount >= count) continue;

                int toAdd = count - currentCount;
                if (toAdd <= 0) continue;

                if (thingDef.stackLimit == 1)
                {
                    for (int i = 0; i < toAdd; i++)
                    {
                        var created = GenerateItem(pawn, spec, kindData);
                        if (created != null && created.holdingOwner == null)
                        {
                            pawn.inventory.innerContainer.TryAdd(created, true);
                        }
                    }
                }
                else
                {
                    var created = GenerateItem(pawn, spec, kindData);
                    if (created != null && created.holdingOwner == null)
                    {
                        created.stackCount = toAdd;
                        while (created.stackCount > 0)
                        {
                            int originalCount = created.stackCount;
                            if (pawn.inventory.innerContainer.TryAdd(created, true))
                            {
                                break;
                            }
                            if (created.stackCount == originalCount)
                            {
                                created.Destroy();
                                break;
                            }
                        }
                    }
                }
            }
        }

        private static bool IsSpecialItem(ThingDef def)
        {
            if (def == null) return false;
            if (def.defName.ToLower().Contains("corpse")) return true;
            return false;
        }

        private static void ApplyHediffs(Pawn pawn, KindGearData kindData)
        {
            if (kindData.ForceOnlySelected && pawn.health != null)
            {
                var hediffsToRemove = pawn.health.hediffSet.hediffs
                    .Where(h => h.def != null && !h.def.defName.StartsWith("Mech"))
                    .ToList();
                
                foreach (var hediff in hediffsToRemove)
                {
                    pawn.health.RemoveHediff(hediff);
                }
            }

            if (kindData.ForcedHediffs.NullOrEmpty()) return;

            foreach (var forcedHediff in kindData.ForcedHediffs)
            {
                if (!Rand.Chance(forcedHediff.chance)) continue;

                if (forcedHediff.IsPool)
                {
                    ApplyHediffFromPool(pawn, forcedHediff);
                    continue;
                }

                if (forcedHediff.HediffDef == null) continue;

                ApplySingleHediff(pawn, forcedHediff);
            }
        }

        private static void ApplySingleHediff(Pawn pawn, ForcedHediff forcedHediff)
        {
            HediffApplicationService.ApplyForcedHediff(pawn, forcedHediff);
        }

        private static bool HediffNeedsBodyPart(HediffDef def)
        {
            if (def == null) return false;
            
            if (def.hediffClass == typeof(Hediff_Level)) return false;
            
            if (def.hediffClass == typeof(Hediff_AddedPart)) return true;
            
            if (def.hediffClass == typeof(Hediff_MissingPart)) return true;
            
            if (def.defName.Contains("Missing")) return true;
            
            if (def.countsAsAddedPartOrImplant) return true;
            
            return false;
        }

        private static void ApplyHediffWithoutPart(Pawn pawn, ForcedHediff forcedHediff)
        {
            HediffDef def = forcedHediff.HediffDef;
            
            if (pawn.health.hediffSet.HasHediff(def)) return;
            
            try
            {
                Hediff hediff = HediffMaker.MakeHediff(def, pawn);
                ApplyHediffSeverity(hediff, forcedHediff);
                
                // 检查是否会使得意识或移动能力低于 50%
                if (!WillHediffIncapacitatePawn(pawn, hediff))
                {
                    pawn.health.AddHediff(hediff);
                }
                else
                {
                    Log.Warning($"[FactionGearCustomizer] Skipped applying hediff {def.defName} to {pawn.Name.ToStringShort} - would reduce consciousness/moving below 50%");
                }
            }
            catch (Exception ex)
            {
                Log.Warning($"[FactionGearCustomizer] Failed to apply hediff {def.defName} without part: {ex.Message}. Trying with a body part...");
                
                try
                {
                    var fallbackPart = pawn.health.hediffSet.GetNotMissingParts().FirstOrDefault();
                    if (fallbackPart != null)
                    {
                        Hediff hediff = HediffMaker.MakeHediff(def, pawn, fallbackPart);
                        ApplyHediffSeverity(hediff, forcedHediff);
                        
                        // 检查是否会使得意识或移动能力低于 50%
                        if (!WillHediffIncapacitatePawn(pawn, hediff))
                        {
                            pawn.health.AddHediff(hediff);
                        }
                        else
                        {
                            Log.Warning($"[FactionGearCustomizer] Skipped applying hediff {def.defName} to {pawn.Name.ToStringShort} - would reduce consciousness/moving below 50%");
                        }
                    }
                }
                catch (Exception ex2)
                {
                    Log.Warning($"[FactionGearCustomizer] Also failed with fallback body part: {ex2.Message}");
                }
            }
        }

        private static void ApplyHediffWithPart(Pawn pawn, ForcedHediff forcedHediff)
        {
            string kindDefName = pawn?.kindDef?.defName ?? "UnknownKind";
            string hediffDefName = forcedHediff?.HediffDef?.defName ?? "UnknownHediff";
            if (pawn?.health?.hediffSet == null)
            {
                Log.Warning($"[FactionGearCustomizer] Skipped hediff {hediffDefName} for {kindDefName}: pawn health state is unavailable.");
                return;
            }

            if (pawn.RaceProps?.body == null)
            {
                Log.Warning($"[FactionGearCustomizer] Skipped hediff {hediffDefName} for {kindDefName}: pawn body definition is unavailable.");
                return;
            }

            if (forcedHediff?.HediffDef == null)
            {
                Log.Warning($"[FactionGearCustomizer] Skipped hediff application for {kindDefName}: forced hediff definition is null.");
                return;
            }

            HediffDef def = forcedHediff.HediffDef;
            
            int count = forcedHediff.maxParts > 0 ? forcedHediff.maxParts : forcedHediff.maxPartsRange.RandomInRange;
            if (count <= 0) count = 1;

            List<BodyPartRecord> userSpecifiedParts = GetUserSpecifiedBodyParts(pawn, forcedHediff);
            
            if (userSpecifiedParts.NullOrEmpty())
            {
                List<BodyPartRecord> validParts = GetValidBodyPartsForHediff(pawn, def);
                
                if (validParts.NullOrEmpty())
                {
                    Log.Warning($"[FactionGearCustomizer] Cannot find valid body parts for hediff {def.defName} on pawn {pawn.Name.ToStringShort}");
                    return;
                }
                
                for (int i = 0; i < count && validParts.Count > 0; i++)
                {
                    if (pawn.health.hediffSet.GetHediffCount(def) >= count) break;
                    
                    BodyPartRecord part = validParts.RandomElement();
                    validParts.Remove(part);
                    
                    if (pawn.health.hediffSet.HasHediff(def, part)) continue;
                    
                    try
                    {
                        Hediff hediff = HediffMaker.MakeHediff(def, pawn, part);
                        ApplyHediffSeverity(hediff, forcedHediff);
                        
                        // 检查是否会使得意识或移动能力低于 50%
                        if (!WillHediffIncapacitatePawn(pawn, hediff))
                        {
                            pawn.health.AddHediff(hediff);
                        }
                        else
                        {
                            Log.Warning($"[FactionGearCustomizer] Skipped applying hediff {def.defName} to {pawn.Name.ToStringShort} - would reduce consciousness/moving below 50%");
                        }
                    }
                    catch (Exception ex)
                    {
                        Log.Warning($"[FactionGearCustomizer] Failed to apply hediff {def.defName} to part {part.Label}: {ex.Message}");
                    }
                }
            }
            else
            {
                count = Mathf.Min(count, userSpecifiedParts.Count);
                
                for (int i = 0; i < count && userSpecifiedParts.Count > 0; i++)
                {
                    BodyPartRecord part = userSpecifiedParts.RandomElement();
                    userSpecifiedParts.Remove(part);
                    
                    if (pawn.health.hediffSet.HasHediff(def, part)) continue;
                    
                    try
                    {
                        Hediff hediff = HediffMaker.MakeHediff(def, pawn, part);
                        ApplyHediffSeverity(hediff, forcedHediff);
                        if (!WillHediffIncapacitatePawn(pawn, hediff))
                        {
                            pawn.health.AddHediff(hediff);
                        }
                        else
                        {
                            Log.Warning($"[FactionGearCustomizer] Skipped applying hediff {def.defName} to {pawn.Name.ToStringShort} - would reduce consciousness/moving below 50%");
                        }
                    }
                    catch (Exception ex)
                    {
                        Log.Warning($"[FactionGearCustomizer] Failed to apply hediff {def.defName} to part {part.Label}: {ex.Message}");
                    }
                }
            }
        }

        private static List<BodyPartRecord> GetUserSpecifiedBodyParts(Pawn pawn, ForcedHediff forcedHediff)
        {
            List<BodyPartRecord> result = new List<BodyPartRecord>();

            if (pawn?.RaceProps?.body == null || pawn.health?.hediffSet == null || forcedHediff?.parts.NullOrEmpty() != false)
            {
                return result;
            }

            foreach (var partDef in forcedHediff.parts)
            {
                if (partDef == null) continue;

                try
                {
                    var parts = pawn.RaceProps.body.GetPartsWithDef(partDef)
                        .Where(p => !pawn.health.hediffSet.PartIsMissing(p))
                        .ToList();
                    result.AddRange(parts);
                }
                catch (Exception ex)
                {
                    Log.Warning($"[FactionGearCustomizer] Failed to resolve user-specified body part {partDef.defName} for {pawn?.kindDef?.defName ?? "UnknownKind"}: {ex.Message}");
                }
            }

            return result;
        }

        private static List<BodyPartRecord> GetValidBodyPartsForHediff(Pawn pawn, HediffDef def)
        {
            List<BodyPartRecord> result = new List<BodyPartRecord>();
            
            if (pawn?.RaceProps?.body == null || def == null) return result;
            
            IEnumerable<BodyPartRecord> allParts = pawn.RaceProps.body.AllParts
                .Where(p => !pawn.health.hediffSet.PartIsMissing(p));
            
            foreach (BodyPartRecord part in allParts)
            {
                try
                {
                    if (def.countsAsAddedPartOrImplant || def.hediffClass == typeof(Hediff_AddedPart))
                    {
                        if (part.def != null && part.def.IsSolid(part, pawn.health.hediffSet.hediffs))
                            continue;
                    }
                    
                    if (!pawn.health.hediffSet.HasHediff(def, part))
                    {
                        result.Add(part);
                    }
                }
                catch
                {
                }
            }
            
            return result;
        }

        private static void ApplyHediffFromPool(Pawn pawn, ForcedHediff poolHediff)
        {
            HediffApplicationService.ApplyForcedHediff(pawn, poolHediff, GetRandomHediffFromPool);
        }

        private static HediffDef GetRandomHediffFromPool(HediffPoolType poolType)
        {
            List<HediffDef> candidates = new List<HediffDef>();

            switch (poolType)
            {
                case HediffPoolType.AnyDebuff:
                    candidates = DefDatabase<HediffDef>.AllDefsListForReading
                        .Where(def => def.isBad && !def.makesSickThought && !def.countsAsAddedPartOrImplant && !def.defName.Contains("Missing"))
                        .ToList();
                    break;

                case HediffPoolType.AnyDrugHigh:
                    candidates = DefDatabase<HediffDef>.AllDefsListForReading
                        .Where(def => (def.hediffClass == typeof(Hediff_High) || def.defName.ToLower().Contains("high")) 
                                     && !def.defName.ToLower().Contains("alcohol"))
                        .ToList();
                    break;

                case HediffPoolType.AnyAddiction:
                    candidates = DefDatabase<HediffDef>.AllDefsListForReading
                        .Where(def => (def.defName.ToLower().Contains("addiction") || def.defName.ToLower().Contains("tolerance"))
                                     && def.hediffClass != typeof(Hediff_High))
                        .ToList();
                    break;

                case HediffPoolType.AnyImplant:
                    candidates = DefDatabase<HediffDef>.AllDefsListForReading
                        .Where(def => def.countsAsAddedPartOrImplant)
                        .ToList();
                    break;

                case HediffPoolType.AnyIllness:
                    candidates = DefDatabase<HediffDef>.AllDefsListForReading
                        .Where(def => def.makesSickThought)
                        .ToList();
                    break;

                case HediffPoolType.AnyBuff:
                    candidates = DefDatabase<HediffDef>.AllDefsListForReading
                        .Where(def => !def.isBad && 
                                     !def.defName.ToLower().Contains("addiction") && 
                                     !def.defName.ToLower().Contains("tolerance") && 
                                     def.hediffClass != typeof(Hediff_High) &&
                                     !def.countsAsAddedPartOrImplant &&
                                     def.hediffClass != typeof(Hediff_AddedPart) &&
                                     def.hediffClass != typeof(Hediff_MissingPart))
                        .ToList();
                    break;
            }

            // 过滤掉会导致危险效果的 Hediff
            candidates = FilterSafeHediffs(candidates);
            
            return candidates.NullOrEmpty() ? null : candidates.RandomElement();
        }

        /// <summary>
        /// 过滤掉会导致危险效果的 Hediff（缺失身体部位、严重昏迷等）
        /// </summary>
        private static List<HediffDef> FilterSafeHediffs(List<HediffDef> hediffs)
        {
            if (hediffs.NullOrEmpty()) return hediffs;
            
            return hediffs.Where(def => IsHediffSafe(def)).ToList();
        }

        /// <summary>
        /// 检查 Hediff 是否安全（不会导致倒地、缺失部位等）
        /// </summary>
        private static bool IsHediffSafe(HediffDef def)
        {
            return HediffApplicationService.IsHediffSafe(def);
        }

        private static void ApplyHediffSeverity(Hediff hediff, ForcedHediff forcedHediff)
        {
            if (forcedHediff.severityRange == default(FloatRange)) return;
            
            float severity = forcedHediff.severityRange.RandomInRange;
            
            if (hediff.def == null) return;
            
            if (hediff.def.lethalSeverity > 0f)
            {
                severity = Mathf.Min(severity, hediff.def.lethalSeverity * 0.95f);
            }
            
            if (!hediff.def.stages.NullOrEmpty())
            {
                float maxStageSeverity = hediff.def.stages.Max(s => s.minSeverity);
                if (maxStageSeverity > 0f && severity > maxStageSeverity)
                {
                    severity = Mathf.Min(severity, maxStageSeverity);
                }
            }
            
            if (severity < 0f) severity = 0f;
            
            hediff.Severity = severity;
        }

        private static void ApplyWeapons(Pawn pawn, KindGearData kindData)
        {
            bool hasAdvancedWeaponData = !kindData.SpecificWeapons.NullOrEmpty();
            bool hasSimpleWeaponData = (kindData.weapons != null && kindData.weapons.Any()) ||
                                       (kindData.meleeWeapons != null && kindData.meleeWeapons.Any());
            bool shouldTouchWeapons = hasAdvancedWeaponData || hasSimpleWeaponData || kindData.ForceNaked || kindData.ForceOnlySelected;
            if (!shouldTouchWeapons) return;

            // Use kind-specific setting if available, otherwise use global setting
            bool forceIgnore = kindData.ForceIgnoreRestrictions ?? FactionGearCustomizerMod.Settings.forceIgnoreRestrictions;

            // Get effective tech level limit
            TechLevel? techLevelLimit = GetEffectiveTechLevelLimit(kindData, pawn);

            // Clear existing weapons if configured or if we are forcing new ones
            if (pawn.equipment != null)
            {
                // Check if we should clear everything
                bool clearAll = kindData.ForceNaked || kindData.ForceOnlySelected;

                var equipmentToDestroy = pawn.equipment.AllEquipmentListForReading
                    .Where(eq => clearAll || eq.def.destroyOnDrop)
                    .ToList();

                foreach (var equipment in equipmentToDestroy)
                {
                    if (pawn.Spawned && pawn.Position.IsValid)
                    {
                        if (!pawn.equipment.TryDropEquipment(equipment, out _, pawn.Position, true))
                        {
                            equipment.Destroy();
                        }
                    }
                    else
                    {
                        equipment.Destroy();
                    }
                }
            }

            // Advanced Mode: SpecificWeapons
            if (hasAdvancedWeaponData)
            {
                foreach (var item in GetWhatToGive(kindData.SpecificWeapons, pawn))
                {
                    if (item.Thing == null) continue;

                    // Tech level check
                    if (!forceIgnore && !IsTechLevelAllowed(item.Thing, techLevelLimit))
                    {
                        continue;
                    }

                    var created = GenerateItem(pawn, item, kindData);
                    if (created is ThingWithComps weapon && pawn.equipment != null)
                    {
                        if (weapon.def.equipmentType == EquipmentType.Primary && pawn.equipment.Primary != null)
                        {
                             pawn.equipment.Remove(pawn.equipment.Primary);
                        }
                        pawn.equipment.AddEquipment(weapon);
                        
                        // CE 兼容性：为需要弹药的武器生成弹药
                        if (CECompat.WeaponNeedsAmmo(weapon))
                        {
                            CECompat.GenerateAndAddAmmoForWeapon(pawn, weapon);
                        }
                    }
                    else if (created != null)
                    {
                        created.Destroy();
                    }
                }
            }
            // Simple Mode: Old Lists (Fallback)
            else if (hasSimpleWeaponData)
            {
                // ... Existing simple logic ...
                if (kindData.weapons.Any())
                {
                    // Filter by tech level
                    var validWeapons = forceIgnore
                        ? kindData.weapons
                        : kindData.weapons.Where(w => IsTechLevelAllowed(w.ThingDef, techLevelLimit)).ToList();

                    if (validWeapons.Any())
                    {
                        var weaponItem = GetRandomGearItem(validWeapons);
                        if (weaponItem?.ThingDef != null)
                        {
                            // Handle conflicting apparel
                            if (forceIgnore && pawn.apparel != null)
                            {
                                var conflictingApparel = pawn.apparel.WornApparel
                                    .Where(a => {
                                        var comp = a.GetComp<CompShield>();
                                        return comp != null && weaponItem.ThingDef.IsRangedWeapon;
                                    })
                                    .ToList();
                                foreach (var apparel in conflictingApparel)
                                {
                                    pawn.apparel.Remove(apparel);
                                    apparel.Destroy();
                                }
                            }

                            var weapon = GenerateSimpleItem(pawn, weaponItem.ThingDef, kindData, true);
                            if (weapon is ThingWithComps wc)
                            {
                                if (wc.def.equipmentType == EquipmentType.Primary && pawn.equipment.Primary != null)
                                {
                                    ThingWithComps primary = pawn.equipment.Primary;
                                    if (pawn.Spawned && pawn.equipment.TryDropEquipment(primary, out var dropped, pawn.Position, false))
                                    {
                                        dropped.Destroy();
                                    }
                                    else
                                    {
                                        pawn.equipment.Remove(primary);
                                        primary.Destroy();
                                    }
                                }
                                pawn.equipment.AddEquipment(wc);
                                
                                // CE 兼容性：为需要弹药的武器生成弹药
                                if (CECompat.WeaponNeedsAmmo(wc))
                                {
                                    CECompat.GenerateAndAddAmmoForWeapon(pawn, wc);
                                }
                            }
                        }
                    }
                }

                if (kindData.meleeWeapons.Any())
                {
                    // Filter by tech level
                    var validMelee = forceIgnore
                        ? kindData.meleeWeapons
                        : kindData.meleeWeapons.Where(w => IsTechLevelAllowed(w.ThingDef, techLevelLimit)).ToList();

                    if (validMelee.Any())
                    {
                        var meleeItem = GetRandomGearItem(validMelee);
                        if (meleeItem?.ThingDef != null)
                        {
                             var weapon = GenerateSimpleItem(pawn, meleeItem.ThingDef, kindData, true);
                            if (weapon is ThingWithComps wc)
                            {
                                if (wc.def.equipmentType == EquipmentType.Primary && pawn.equipment.Primary != null)
                                {
                                    ThingWithComps primary = pawn.equipment.Primary;
                                    if (pawn.Spawned && pawn.equipment.TryDropEquipment(primary, out var dropped, pawn.Position, false))
                                    {
                                        dropped.Destroy();
                                    }
                                    else
                                    {
                                        pawn.equipment.Remove(primary);
                                        primary.Destroy();
                                    }
                                }
                                pawn.equipment.AddEquipment(wc);
                                
                                // CE 兼容性：为需要弹药的武器生成弹药（某些近战武器可能需要）
                                if (CECompat.WeaponNeedsAmmo(wc))
                                {
                                    CECompat.GenerateAndAddAmmoForWeapon(pawn, wc);
                                }
                            }
                        }
                    }
                }
            }
        }

        private static void ApplyApparel(Pawn pawn, KindGearData kindData)
        {
            if (kindData.ForceNaked)
            {
                SafeDestroyAllApparel(pawn);
                return;
            }

            // Filter invalid entries first to keep the apply pipeline deterministic.
            var requiredApparel = kindData.ApparelRequired?
                .Where(def => def != null)
                .ToList();
            var specificApparel = kindData.SpecificApparel?
                .Where(spec => spec != null && spec.Thing != null)
                .ToList();
            var simpleArmors = kindData.armors?
                .Where(item => item != null && item.ThingDef != null)
                .ToList();
            var simpleApparel = kindData.apparel?
                .Where(item => item != null && item.ThingDef != null)
                .ToList();
            var simpleOthers = kindData.others?
                .Where(item => item != null && item.ThingDef != null)
                .ToList();

            // Check if we have ANY data to apply. If not, don't strip (unless forced).
            bool hasAdvancedData = !specificApparel.NullOrEmpty() || !requiredApparel.NullOrEmpty();
            bool hasSimpleData = !simpleArmors.NullOrEmpty() || !simpleApparel.NullOrEmpty() || !simpleOthers.NullOrEmpty();

            if (!hasAdvancedData && !hasSimpleData && !kindData.ForceOnlySelected)
            {
                return;
            }

            // Get effective tech level limit
            TechLevel? techLevelLimit = GetEffectiveTechLevelLimit(kindData, pawn);
            bool forceIgnore = kindData.ForceIgnoreRestrictions ?? FactionGearCustomizerMod.Settings.forceIgnoreRestrictions;

            if (kindData.OutfitFirstBudgetStrategy && (hasAdvancedData || hasSimpleData))
            {
                ApplyApparelWithOutfitFirstStrategy(
                    pawn,
                    kindData,
                    requiredApparel,
                    specificApparel,
                    simpleArmors,
                    simpleApparel,
                    simpleOthers,
                    hasAdvancedData,
                    hasSimpleData,
                    forceIgnore,
                    techLevelLimit);
                return;
            }

            // Strip logic
            if (kindData.ForceOnlySelected)
            {
                 // Strip everything that isn't required (complex check, simplified here to strip all then re-add)
                 // Actually, TotalControl logic is: "Destroy what is worn that is NOT in the allowed list".
                 // For now, let's stick to: Strip all, then add what we want.
                 SafeDestroyAllApparel(pawn);
            }
            else
            {
                // Only destroy "destroyOnDrop" items (vanilla behavior for pawns)
                var apparelToDestroy = pawn.apparel.WornApparel
                    .Where(app => app.def != null && app.def.destroyOnDrop && app.def.destroyable)
                    .ToList();
                foreach (var apparel in apparelToDestroy)
                {
                    pawn.apparel.Remove(apparel);
                    apparel.Destroy();
                }
            }

            // Advanced Mode: SpecificApparel
            if (hasAdvancedData)
            {
                // Budget Logic (Advanced Mode)
                float budget = kindData.ApparelMoney?.RandomInRange ?? pawn.kindDef?.apparelMoney.RandomInRange ?? 0f;
                float currentSpent = 0f;
                if (pawn.apparel != null)
                {
                    currentSpent = pawn.apparel.WornApparel.Sum(a => a.MarketValue);
                }

                // Simple Required List
                if (!requiredApparel.NullOrEmpty())
                {
                    foreach (var def in requiredApparel)
                    {
                        // Tech level check
                        if (!forceIgnore && !IsTechLevelAllowed(def, techLevelLimit))
                        {
                            continue;
                        }

                        var item = new SpecRequirementEdit() { Thing = def, SelectionMode = ApparelSelectionMode.AlwaysTake };
                        var created = GenerateItem(pawn, item, kindData, forceIgnore ? -1f : (budget - currentSpent));
                        if (created is Apparel app && CanWearApparel(pawn, app.def))
                        {
                            // 跳过不可摧毁的物品（如 Apparel_CerebrexNode），避免 InfoCard 报错
                            if (!app.def.destroyable)
                            {
                                created.Destroy();
                                continue;
                            }

                            if (!forceIgnore && (currentSpent + app.MarketValue > budget))
                            {
                                // Log.Warning($"[FactionGearCustomizer] Budget exceeded for {pawn}: Budget={budget}, Current={currentSpent}, Item={app.LabelCap} ({app.MarketValue}). Skipping.");
                                created.Destroy();
                                continue;
                            }

                            try
                            {
                                pawn.apparel.Wear(app, true);
                                currentSpent += app.MarketValue;
                            }
                            catch (Exception ex)
                            {
                                Log.Warning($"[FactionGearCustomizer] Failed to wear {app} on {pawn}: {ex.Message}");
                                app.Destroy();
                            }
                        }
                    }
                }

                // Complex Specific List
                if (!specificApparel.NullOrEmpty())
                {
                    foreach (var item in GetWhatToGive(specificApparel, pawn))
                    {
                         if (item?.Thing == null) continue;

                         // Tech level check
                         if (!forceIgnore && !IsTechLevelAllowed(item.Thing, techLevelLimit))
                         {
                             continue;
                         }

                         var created = GenerateItem(pawn, item, kindData, forceIgnore ? -1f : (budget - currentSpent));
                         if (created is Apparel app && CanWearApparel(pawn, app.def))
                         {
                             // 跳过不可摧毁的物品（如 Apparel_CerebrexNode），避免 InfoCard 报错
                             if (!app.def.destroyable)
                             {
                                 created.Destroy();
                                 continue;
                             }

                             // Check Budget
                             if (!forceIgnore && (currentSpent + app.MarketValue > budget))
                             {
                                 // Log.Warning($"[FactionGearCustomizer] Budget exceeded for {pawn}: Budget={budget}, Current={currentSpent}, Item={app.LabelCap} ({app.MarketValue}). Skipping.");
                                 created.Destroy();
                                 continue;
                             }

                             try
                             {
                                 pawn.apparel.Wear(app, true);
                                 currentSpent += app.MarketValue;
                             }
                             catch (Exception ex)
                             {
                                 Log.Warning($"[FactionGearCustomizer] Failed to wear {app} on {pawn}: {ex.Message}");
                                 app.Destroy();
                             }
                         }
                    }
                }
            }
            // Simple Mode: Old Lists (Fallback)
            if (!hasAdvancedData && hasSimpleData)
            {
                // Fix: Always try to equip multiple items from the list.
                // This allows users to specify multiple items (e.g. helmet + armor) in the same list
                // and have them all applied if they don't conflict.
                // Previously this was only enabled for ForceIgnore, but it should be the default behavior
                // as users expect "what they list is what they get" (limited by slots/conflicts).
                bool tryEquipMultiple = true;

                // Budget Logic
                float budget = kindData.ApparelMoney?.RandomInRange ?? pawn.kindDef?.apparelMoney.RandomInRange ?? 0f;
                float currentSpent = 0f;
                if (pawn.apparel != null)
                {
                    currentSpent = pawn.apparel.WornApparel.Sum(a => a.MarketValue);
                }

                // Log budget info for debugging
                // Log.Message($"[FactionGearCustomizer] Budget Check for {pawn}: Budget={budget}, CurrentSpent={currentSpent}, ForceIgnore={forceIgnore}");

                void EquipApparelList(List<GearItem> gearList)
                {
                    if (gearList.NullOrEmpty()) return;

                    // Filter by tech level
                    var validList = gearList
                        .Where(g => g != null && g.ThingDef != null)
                        .Where(g => forceIgnore || IsTechLevelAllowed(g.ThingDef, techLevelLimit))
                        .ToList();

                    if (validList.NullOrEmpty()) return;

                    if (tryEquipMultiple)
                    {
                        var workingList = validList.ToList();
                        // Max attempts to prevent any infinite loops, though list size is natural limit
                        int maxAttempts = workingList.Count * 2;
                        int attempts = 0;

                        while (workingList.Any() && attempts < maxAttempts)
                        {
                            attempts++;
                            var item = GetRandomGearItem(workingList);
                            if (item == null) break;

                            // Remove from working list so we don't pick it again
                            workingList.Remove(item);

                            if (item.ThingDef != null)
                            {
                                var created = GenerateSimpleItem(pawn, item.ThingDef, kindData, false, forceIgnore ? -1f : (budget - currentSpent));
                                if (created is Apparel app)
                                {
                                    // 跳过不可摧毁的物品（如 Apparel_CerebrexNode），避免 InfoCard 报错
                                    if (!app.def.destroyable)
                                    {
                                        created.Destroy();
                                        continue;
                                    }

                                    // Check Budget (unless ignored)
                                    if (!forceIgnore && (currentSpent + app.MarketValue > budget))
                                    {
                                        // Log.Warning($"[FactionGearCustomizer] Budget exceeded for {pawn}: Budget={budget}, Current={currentSpent}, Item={app.LabelCap} ({app.MarketValue}). Skipping.");
                                        created.Destroy();
                                        continue;
                                    }

                                    if (CanWearApparel(pawn, app.def))
                                    {
                                        try
                                        {
                                            pawn.apparel.Wear(app, true);
                                            currentSpent += app.MarketValue;
                                        }
                                        catch (Exception ex)
                                        {
                                            Log.Warning($"[FactionGearCustomizer] Failed to wear {app} on {pawn}: {ex.Message}");
                                            app.Destroy();
                                        }
                                    }
                                    else
                                    {
                                        created.Destroy();
                                    }
                                }
                                else if (created != null)
                                {
                                    created.Destroy();
                                }
                            }
                        }
                    }
                    else
                    {
                        var item = GetRandomGearItem(validList);
                        if (item?.ThingDef != null)
                        {
                            var created = GenerateSimpleItem(pawn, item.ThingDef, kindData, false, forceIgnore ? -1f : (budget - currentSpent));
                            if (created is Apparel app)
                            {
                                // 跳过不可摧毁的物品（如 Apparel_CerebrexNode），避免 InfoCard 报错
                                if (!app.def.destroyable)
                                {
                                    created.Destroy();
                                    return;
                                }

                                // Check Budget (unless ignored)
                                if (!forceIgnore && (currentSpent + app.MarketValue > budget))
                                {
                                    // Log.Warning($"[FactionGearCustomizer] Budget exceeded for {pawn}: Budget={budget}, Current={currentSpent}, Item={app.LabelCap} ({app.MarketValue}). Skipping.");
                                    created.Destroy();
                                    return;
                                }

                                if (CanWearApparel(pawn, app.def))
                                {
                                    try
                                    {
                                        pawn.apparel.Wear(app, true);
                                        currentSpent += app.MarketValue;
                                    }
                                    catch (Exception ex)
                                    {
                                        Log.Warning($"[FactionGearCustomizer] Failed to wear {app} on {pawn}: {ex.Message}");
                                        app.Destroy();
                                    }
                                }
                                else
                                {
                                    created.Destroy();
                                }
                            }
                            else if (created != null)
                            {
                                created.Destroy();
                            }
                        }
                    }
                }

                EquipApparelList(simpleArmors);
                EquipApparelList(simpleApparel);
                EquipApparelList(simpleOthers);
            }
        }

        private static void ApplyApparelWithOutfitFirstStrategy(
            Pawn pawn,
            KindGearData kindData,
            List<ThingDef> requiredApparel,
            List<SpecRequirementEdit> specificApparel,
            List<GearItem> simpleArmors,
            List<GearItem> simpleApparel,
            List<GearItem> simpleOthers,
            bool hasAdvancedData,
            bool hasSimpleData,
            bool forceIgnore,
            TechLevel? techLevelLimit)
        {
            float budget = kindData.ApparelMoney?.max ?? pawn.kindDef?.apparelMoney.max ?? 0f;

            var coreSpecs = hasAdvancedData
                ? CollectAdvancedCoreSpecs(requiredApparel, specificApparel, pawn)
                : BuildSimpleCoreSpecs(simpleArmors, simpleApparel, simpleOthers);

            if (!TryBuildCoreOutfitPlan(
                pawn,
                kindData,
                coreSpecs,
                forceIgnore,
                techLevelLimit,
                budget,
                DefaultArmorBudgetFloorRatio,
                out var corePlan,
                out string reason))
            {
                Log.Warning($"[FactionGearCustomizer] Core outfit planning failed for {pawn?.Name?.ToStringShort ?? "Unknown"} ({pawn?.kindDef?.defName ?? "UnknownKind"}): {reason}");
                if (kindData.ForceOnlySelected)
                {
                    ApplyStrictPoolFallbackApparel(
                        pawn,
                        kindData,
                        requiredApparel,
                        specificApparel,
                        simpleArmors,
                        simpleApparel,
                        simpleOthers,
                        hasAdvancedData,
                        hasSimpleData,
                        forceIgnore,
                        techLevelLimit,
                        budget,
                        "Core outfit planning failed",
                        true);
                }
                return;
            }

            if (!string.IsNullOrEmpty(reason))
            {
                Log.Warning($"[FactionGearCustomizer] Outfit-first budget fallback for {pawn?.Name?.ToStringShort ?? "Unknown"}: {reason}");
            }

            if (kindData.ForceOnlySelected)
            {
                SafeDestroyAllApparel(pawn);
            }
            else
            {
                var apparelToDestroy = pawn.apparel.WornApparel
                    .Where(app => app.def != null && app.def.destroyOnDrop && app.def.destroyable)
                    .ToList();
                foreach (var apparel in apparelToDestroy)
                {
                    pawn.apparel.Remove(apparel);
                    apparel.Destroy();
                }
            }

            float currentSpent = pawn.apparel?.WornApparel?.Sum(a => a.MarketValue) ?? 0f;
            var protectedCoreDefs = new HashSet<ThingDef>();
            var equippedCore = EquipCoreOutfit(pawn, kindData, corePlan, ref currentSpent, protectedCoreDefs);
            if (equippedCore.Count < CoreSlotOrder.Length)
            {
                Log.Warning($"[FactionGearCustomizer] Core outfit generation incomplete for {pawn?.Name?.ToStringShort ?? "Unknown"} ({pawn?.kindDef?.defName ?? "UnknownKind"}).");
                if (kindData.ForceOnlySelected)
                {
                    ApplyStrictPoolFallbackApparel(
                        pawn,
                        kindData,
                        requiredApparel,
                        specificApparel,
                        simpleArmors,
                        simpleApparel,
                        simpleOthers,
                        hasAdvancedData,
                        hasSimpleData,
                        forceIgnore,
                        techLevelLimit,
                        budget,
                        "Core outfit generation incomplete",
                        false,
                        currentSpent,
                        protectedCoreDefs);
                }
                return;
            }

            ApplyCoreMaterialUpgrades(pawn, kindData, equippedCore, ref currentSpent, budget, forceIgnore);

            if (hasAdvancedData)
            {
                ApplyAdvancedAdditionalApparel(
                    pawn,
                    kindData,
                    requiredApparel,
                    specificApparel,
                    forceIgnore,
                    techLevelLimit,
                    budget,
                    ref currentSpent,
                    protectedCoreDefs);
            }
            else if (hasSimpleData)
            {
                ApplySimpleAdditionalApparel(
                    pawn,
                    kindData,
                    simpleArmors,
                    simpleApparel,
                    simpleOthers,
                    forceIgnore,
                    techLevelLimit,
                    budget,
                    ref currentSpent,
                    protectedCoreDefs);
            }
        }

        private static void ApplyStrictPoolFallbackApparel(
            Pawn pawn,
            KindGearData kindData,
            List<ThingDef> requiredApparel,
            List<SpecRequirementEdit> specificApparel,
            List<GearItem> simpleArmors,
            List<GearItem> simpleApparel,
            List<GearItem> simpleOthers,
            bool hasAdvancedData,
            bool hasSimpleData,
            bool forceIgnore,
            TechLevel? techLevelLimit,
            float budget,
            string triggerReason,
            bool stripExisting,
            float currentSpent = 0f,
            HashSet<ThingDef> protectedCoreDefs = null)
        {
            if (pawn?.apparel == null)
            {
                Log.Warning($"[FactionGearCustomizer] Strict pool fallback skipped for {pawn?.kindDef?.defName ?? "UnknownKind"} because apparel tracker is unavailable.");
                return;
            }

            if (stripExisting)
            {
                SafeDestroyAllApparel(pawn);
                currentSpent = 0f;
            }

            protectedCoreDefs = protectedCoreDefs ?? new HashSet<ThingDef>();

            if (hasAdvancedData)
            {
                ApplyAdvancedAdditionalApparel(
                    pawn,
                    kindData,
                    requiredApparel,
                    specificApparel,
                    forceIgnore,
                    techLevelLimit,
                    budget,
                    ref currentSpent,
                    protectedCoreDefs);
            }

            if (hasSimpleData)
            {
                ApplySimpleAdditionalApparel(
                    pawn,
                    kindData,
                    simpleArmors,
                    simpleApparel,
                    simpleOthers,
                    forceIgnore,
                    techLevelLimit,
                    budget,
                    ref currentSpent,
                    protectedCoreDefs);
            }

            int equippedCount = pawn.apparel.WornApparel?.Count ?? 0;
            if (equippedCount > 0)
            {
                Log.Warning($"[FactionGearCustomizer] Strict pool fallback applied for {pawn?.Name?.ToStringShort ?? "Unknown"} ({pawn?.kindDef?.defName ?? "UnknownKind"}) after '{triggerReason}'. Equipped {equippedCount} pool-only apparel item(s).");
            }
            else
            {
                Log.Warning($"[FactionGearCustomizer] Strict pool fallback found no valid apparel candidates for {pawn?.Name?.ToStringShort ?? "Unknown"} ({pawn?.kindDef?.defName ?? "UnknownKind"}) after '{triggerReason}'.");
            }
        }

        private static List<SpecRequirementEdit> CollectAdvancedCoreSpecs(List<ThingDef> requiredApparel, List<SpecRequirementEdit> specificApparel, Pawn pawn)
        {
            var result = new List<SpecRequirementEdit>();
            if (!requiredApparel.NullOrEmpty())
            {
                result.AddRange(requiredApparel.Select(CreateRequiredSpec));
            }

            if (specificApparel.NullOrEmpty())
            {
                return result;
            }

            result.AddRange(specificApparel.Where(item => item.SelectionMode == ApparelSelectionMode.AlwaysTake));
            AddCorePoolSelection(result, specificApparel, ApparelSelectionMode.FromPool1, pawn);
            AddCorePoolSelection(result, specificApparel, ApparelSelectionMode.FromPool2, pawn);
            AddCorePoolSelection(result, specificApparel, ApparelSelectionMode.FromPool3, pawn);
            AddCorePoolSelection(result, specificApparel, ApparelSelectionMode.FromPool4, pawn);
            return result;
        }

        private static void AddCorePoolSelection(List<SpecRequirementEdit> result, List<SpecRequirementEdit> specificApparel, ApparelSelectionMode mode, Pawn pawn)
        {
            var pool = specificApparel.Where(item => item.SelectionMode == mode).ToList();
            var selected = PickFromPool(pool, pawn);
            if (selected != null)
            {
                result.Add(selected);
            }
        }

        private static List<SpecRequirementEdit> BuildSimpleCoreSpecs(List<GearItem> simpleArmors, List<GearItem> simpleApparel, List<GearItem> simpleOthers)
        {
            var result = new List<SpecRequirementEdit>();
            var seen = new HashSet<string>();
            AddSimpleCoreSpecs(result, simpleArmors, seen);
            AddSimpleCoreSpecs(result, simpleApparel, seen);
            AddSimpleCoreSpecs(result, simpleOthers, seen);
            return result;
        }

        private static void AddSimpleCoreSpecs(List<SpecRequirementEdit> result, List<GearItem> source, HashSet<string> seen)
        {
            if (source.NullOrEmpty())
            {
                return;
            }

            foreach (var item in source)
            {
                if (item?.ThingDef == null) continue;
                if (!seen.Add(item.ThingDef.defName)) continue;
                result.Add(new SpecRequirementEdit { Thing = item.ThingDef, SelectionMode = ApparelSelectionMode.AlwaysTake });
            }
        }

        private static SpecRequirementEdit CreateRequiredSpec(ThingDef def)
        {
            return new SpecRequirementEdit
            {
                Thing = def,
                SelectionMode = ApparelSelectionMode.AlwaysTake
            };
        }

        private static bool TryBuildCoreOutfitPlan(
            Pawn pawn,
            KindGearData kindData,
            List<SpecRequirementEdit> coreSpecs,
            bool forceIgnore,
            TechLevel? techLevelLimit,
            float budget,
            float armorBudgetFloorRatio,
            out List<CoreOutfitCandidate> corePlan,
            out string reason)
        {
            corePlan = null;
            var bySlot = CoreSlotOrder.ToDictionary(slot => slot, _ => new List<CoreOutfitCandidate>());
            if (coreSpecs.NullOrEmpty())
            {
                reason = "No valid apparel candidates for core outfit.";
                return false;
            }

            foreach (var spec in coreSpecs)
            {
                if (spec?.Thing == null || !spec.Thing.IsApparel) continue;
                if (!CanWearApparel(pawn, spec.Thing)) continue;
                if (!forceIgnore && !IsTechLevelAllowed(spec.Thing, techLevelLimit)) continue;

                QualityCategory quality = ResolveApparelQuality(spec, kindData);
                var stuffOptions = GetCoreStuffOptions(spec, quality);
                foreach (var stuff in stuffOptions)
                {
                    float estimate = EstimateConfiguredMarketValue(spec.Thing, stuff, quality);
                    foreach (var slot in GetCoreSlots(spec.Thing))
                    {
                        bySlot[slot].Add(new CoreOutfitCandidate
                        {
                            Slot = slot,
                            Spec = spec,
                            Stuff = stuff,
                            Quality = quality,
                            EstimatedValue = estimate
                        });
                    }
                }
            }

            foreach (var slot in CoreSlotOrder)
            {
                bySlot[slot] = TrimCoreCandidatesForSearch(bySlot[slot]);
                if (bySlot[slot].Count == 0)
                {
                    reason = $"Missing core layer candidate: {slot}.";
                    return false;
                }
            }

            if (!TrySelectBestCoreCombinationByBudget(bySlot, budget, armorBudgetFloorRatio, out corePlan, out reason))
            {
                reason = "No non-conflicting core outfit combination available.";
                return false;
            }
            return true;
        }

        private static IEnumerable<CoreOutfitSlot> GetCoreSlots(ThingDef def)
        {
            if (IsTorsoInnerCandidate(def)) yield return CoreOutfitSlot.TorsoInner;
            if (IsLegsInnerCandidate(def)) yield return CoreOutfitSlot.LegsInner;
            if (IsArmorMiddleCandidate(def)) yield return CoreOutfitSlot.ArmorMiddle;
            if (IsShellCandidate(def)) yield return CoreOutfitSlot.Shell;
        }

        private static bool IsTorsoInnerCandidate(ThingDef def)
        {
            return CoversBodyPart(def, BodyPartGroupDefOf.Torso)
                && (HasLayer(def, ApparelLayerDefOf.OnSkin) || HasLayer(def, ApparelLayerDefOf.Middle));
        }

        private static bool IsLegsInnerCandidate(ThingDef def)
        {
            return CoversBodyPart(def, GroupLegs)
                && (HasLayer(def, ApparelLayerDefOf.OnSkin) || HasLayer(def, ApparelLayerDefOf.Middle));
        }

        private static bool IsArmorMiddleCandidate(ThingDef def)
        {
            return CoversBodyPart(def, BodyPartGroupDefOf.Torso)
                && HasLayer(def, ApparelLayerDefOf.Middle)
                && def.GetStatValueAbstract(StatDefOf.ArmorRating_Sharp) > 0.4f;
        }

        private static bool IsShellCandidate(ThingDef def)
        {
            return CoversBodyPart(def, BodyPartGroupDefOf.Torso)
                && HasLayer(def, ApparelLayerDefOf.Shell);
        }

        private static bool CoversBodyPart(ThingDef def, BodyPartGroupDef group)
        {
            return group != null
                && def?.apparel?.bodyPartGroups != null
                && def.apparel.bodyPartGroups.Contains(group);
        }

        private static bool HasLayer(ThingDef def, ApparelLayerDef layer)
        {
            return def?.apparel?.layers != null && def.apparel.layers.Contains(layer);
        }

        private static List<CoreOutfitCandidate> TrimCoreCandidatesForSearch(List<CoreOutfitCandidate> candidates)
        {
            if (candidates.NullOrEmpty())
            {
                return new List<CoreOutfitCandidate>();
            }

            var ordered = candidates.OrderByDescending(c => c.EstimatedValue).ToList();
            if (ordered.Count <= MaxCoreCandidatesPerSlot)
            {
                return ordered;
            }

            var trimmed = ordered.Take(MaxCoreCandidatesPerSlot - 1).ToList();
            var cheapest = ordered[ordered.Count - 1];
            if (!trimmed.Contains(cheapest))
            {
                trimmed.Add(cheapest);
            }
            return trimmed;
        }

        private static bool TrySelectBestCoreCombinationByBudget(
            Dictionary<CoreOutfitSlot, List<CoreOutfitCandidate>> bySlot,
            float budget,
            float armorBudgetFloorRatio,
            out List<CoreOutfitCandidate> selected,
            out string reason)
        {
            selected = null;
            reason = null;

            var ordered = CoreSlotOrder.ToDictionary(
                slot => slot,
                slot => bySlot[slot].OrderByDescending(c => c.EstimatedValue).ToList());

            List<CoreOutfitCandidate> bestFitWithArmor = null;
            float bestFitWithArmorCost = -1f;

            List<CoreOutfitCandidate> bestFitNoArmor = null;
            float bestFitNoArmorCost = -1f;
            float bestFitNoArmorArmorValue = -1f;

            List<CoreOutfitCandidate> cheapestOverall = null;
            float cheapestOverallCost = float.MaxValue;

            float armorFloor = Mathf.Max(0f, budget * armorBudgetFloorRatio);

            void Evaluate(List<CoreOutfitCandidate> current, float totalCost, float armorCost)
            {
                if (totalCost < cheapestOverallCost)
                {
                    cheapestOverallCost = totalCost;
                    cheapestOverall = current.ToList();
                }

                if (totalCost > budget) return;

                if (armorCost >= armorFloor)
                {
                    if (totalCost > bestFitWithArmorCost)
                    {
                        bestFitWithArmorCost = totalCost;
                        bestFitWithArmor = current.ToList();
                    }
                    return;
                }

                if (totalCost > bestFitNoArmorCost || (Mathf.Abs(totalCost - bestFitNoArmorCost) < 0.01f && armorCost > bestFitNoArmorArmorValue))
                {
                    bestFitNoArmorCost = totalCost;
                    bestFitNoArmorArmorValue = armorCost;
                    bestFitNoArmor = current.ToList();
                }
            }

            void Search(int index, List<CoreOutfitCandidate> current, float totalCost, float armorCost)
            {
                if (index == CoreSlotOrder.Length)
                {
                    Evaluate(current, totalCost, armorCost);
                    return;
                }

                foreach (var candidate in ordered[CoreSlotOrder[index]])
                {
                    if (current.Any(x => x.Spec?.Thing == candidate.Spec?.Thing)) continue;
                    if (current.Any(x => !AreApparelDefsCompatible(x.Spec?.Thing, candidate.Spec?.Thing))) continue;

                    float nextTotal = totalCost + candidate.EstimatedValue;
                    float nextArmor = armorCost + (IsArmorSlot(candidate.Slot) ? candidate.EstimatedValue : 0f);

                    current.Add(candidate);
                    Search(index + 1, current, nextTotal, nextArmor);
                    current.RemoveAt(current.Count - 1);
                }
            }

            Search(0, new List<CoreOutfitCandidate>(), 0f, 0f);

            if (bestFitWithArmor != null)
            {
                selected = bestFitWithArmor;
                return true;
            }

            if (bestFitNoArmor != null)
            {
                selected = bestFitNoArmor;
                reason = $"Armor floor fallback: target armor >= {armorFloor:F1}, actual {bestFitNoArmorArmorValue:F1}.";
                return true;
            }

            if (cheapestOverall != null)
            {
                selected = cheapestOverall;
                reason = $"Budget floor fallback: cheapest core outfit {cheapestOverallCost:F1} exceeds budget {budget:F1}.";
                return true;
            }

            return false;
        }

        private static bool IsArmorSlot(CoreOutfitSlot slot)
        {
            return slot == CoreOutfitSlot.ArmorMiddle || slot == CoreOutfitSlot.Shell;
        }

        private static bool AreApparelDefsCompatible(ThingDef left, ThingDef right)
        {
            if (left == null || right == null || left.apparel == null || right.apparel == null) return false;
            if (left == right) return false;

            bool shareLayer = left.apparel.layers != null
                && right.apparel.layers != null
                && left.apparel.layers.Intersect(right.apparel.layers).Any();
            if (!shareLayer) return true;

            var leftGroups = left.apparel.bodyPartGroups;
            var rightGroups = right.apparel.bodyPartGroups;
            if (leftGroups.NullOrEmpty() || rightGroups.NullOrEmpty()) return false;

            bool shareGroup = leftGroups.Intersect(rightGroups).Any();
            return !shareGroup;
        }

        private static QualityCategory ResolveApparelQuality(SpecRequirementEdit spec, KindGearData kindData)
        {
            return spec?.Quality ?? kindData?.ItemQuality ?? QualityCategory.Normal;
        }

        private static IEnumerable<ThingDef> GetCoreStuffOptions(SpecRequirementEdit spec, QualityCategory quality)
        {
            if (spec?.Thing == null)
            {
                yield break;
            }

            if (spec.Material != null)
            {
                yield return spec.Material;
                yield break;
            }

            if (!spec.Thing.MadeFromStuff)
            {
                yield return null;
                yield break;
            }

            var allowed = GenStuff.AllowedStuffsFor(spec.Thing)?.Where(stuff => stuff != null).ToList();
            if (allowed.NullOrEmpty())
            {
                ThingDef fallback = GenStuff.RandomStuffFor(spec.Thing);
                if (fallback != null)
                {
                    yield return fallback;
                }
                yield break;
            }

            foreach (var stuff in allowed.OrderByDescending(stuff => EstimateConfiguredMarketValue(spec.Thing, stuff, quality)))
            {
                yield return stuff;
            }
        }

        private static float EstimateConfiguredMarketValue(ThingDef def, ThingDef stuff, QualityCategory quality)
        {
            if (def == null) return 0f;
            if (!def.HasComp(typeof(CompQuality)))
            {
                return GetEstimatedMarketValue(def, stuff, QualityCategory.Normal);
            }
            return GetEstimatedMarketValue(def, stuff, quality);
        }

        private static List<EquippedCoreItem> EquipCoreOutfit(
            Pawn pawn,
            KindGearData kindData,
            List<CoreOutfitCandidate> corePlan,
            ref float currentSpent,
            HashSet<ThingDef> protectedCoreDefs)
        {
            var equipped = new List<EquippedCoreItem>();
            foreach (var slot in CoreSlotOrder)
            {
                var selected = corePlan.FirstOrDefault(item => item.Slot == slot);
                if (selected == null) continue;

                var spec = CloneSpecForGeneration(selected.Spec, selected.Stuff, selected.Quality);
                var created = GenerateItem(pawn, spec, kindData, -1f);
                if (created is Apparel app)
                {
                    if (TryWearCoreApparel(pawn, app, ref currentSpent, protectedCoreDefs))
                    {
                        equipped.Add(new EquippedCoreItem
                        {
                            Slot = slot,
                            Spec = selected.Spec,
                            Apparel = app
                        });
                    }
                }
                else
                {
                    created?.Destroy();
                }
            }
            return equipped;
        }

        private static bool TryWearCoreApparel(Pawn pawn, Apparel app, ref float currentSpent, HashSet<ThingDef> protectedCoreDefs)
        {
            if (app == null) return false;
            if (!app.def.destroyable || !CanWearApparel(pawn, app.def))
            {
                app.Destroy();
                return false;
            }

            if (WouldConflictWithCoreSet(pawn, app.def, protectedCoreDefs))
            {
                app.Destroy();
                return false;
            }

            try
            {
                pawn.apparel.Wear(app, true);
                protectedCoreDefs.Add(app.def);
                currentSpent = pawn.apparel.WornApparel.Sum(a => a.MarketValue);
                return true;
            }
            catch (Exception ex)
            {
                Log.Warning($"[FactionGearCustomizer] Failed to wear core apparel {app} on {pawn}: {ex.Message}");
                app.Destroy();
                return false;
            }
        }

        private static void ApplyCoreMaterialUpgrades(
            Pawn pawn,
            KindGearData kindData,
            List<EquippedCoreItem> equippedCore,
            ref float currentSpent,
            float budget,
            bool forceIgnore)
        {
            foreach (var entry in equippedCore)
            {
                if (entry?.Apparel?.def == null || entry.Spec == null) continue;
                if (entry.Spec.Material != null) continue;
                if (!entry.Apparel.def.MadeFromStuff) continue;

                var currentApparel = entry.Apparel;
                var currentStuff = currentApparel.Stuff;
                var quality = GetCurrentQuality(currentApparel);
                float currentValue = currentApparel.MarketValue;

                var upgrades = GenStuff.AllowedStuffsFor(currentApparel.def)?
                    .Where(stuff => stuff != null && stuff != currentStuff)
                    .Select(stuff => new
                    {
                        Stuff = stuff,
                        Value = EstimateConfiguredMarketValue(currentApparel.def, stuff, quality)
                    })
                    .Where(x => x.Value > currentValue + 0.01f)
                    .OrderByDescending(x => x.Value)
                    .ToList();

                if (upgrades.NullOrEmpty()) continue;

                foreach (var upgrade in upgrades)
                {
                    if (!forceIgnore)
                    {
                        float projected = currentSpent - currentValue + upgrade.Value;
                        if (projected > budget) continue;
                    }

                    var replacementSpec = CloneSpecForGeneration(entry.Spec, upgrade.Stuff, quality);
                    var created = GenerateItem(
                        pawn,
                        replacementSpec,
                        kindData,
                        forceIgnore ? -1f : (budget - (currentSpent - currentValue)));

                    if (!(created is Apparel replacement))
                    {
                        created?.Destroy();
                        continue;
                    }

                    if (!replacement.def.destroyable || !CanWearApparel(pawn, replacement.def))
                    {
                        replacement.Destroy();
                        continue;
                    }

                    try
                    {
                        pawn.apparel.Wear(replacement, true);
                        if (pawn.apparel.WornApparel.Contains(currentApparel))
                        {
                            pawn.apparel.Remove(currentApparel);
                        }
                        if (!currentApparel.Destroyed)
                        {
                            currentApparel.Destroy();
                        }

                        entry.Apparel = replacement;
                        currentSpent = pawn.apparel.WornApparel.Sum(a => a.MarketValue);
                        break;
                    }
                    catch (Exception ex)
                    {
                        Log.Warning($"[FactionGearCustomizer] Failed to upgrade material for {currentApparel?.def?.defName}: {ex.Message}");
                        replacement.Destroy();
                    }
                }
            }
        }

        private static QualityCategory GetCurrentQuality(Apparel apparel)
        {
            if (apparel == null) return QualityCategory.Normal;
            var compQuality = apparel.TryGetComp<CompQuality>();
            return compQuality?.Quality ?? QualityCategory.Normal;
        }

        private static SpecRequirementEdit CloneSpecForGeneration(SpecRequirementEdit source, ThingDef forcedStuff, QualityCategory? forcedQuality)
        {
            if (source == null) return null;
            return new SpecRequirementEdit
            {
                Thing = source.Thing,
                Material = forcedStuff ?? source.Material,
                Style = source.Style,
                Quality = forcedQuality ?? source.Quality,
                Biocode = source.Biocode,
                Color = source.Color,
                SelectionMode = source.SelectionMode,
                SelectionChance = source.SelectionChance,
                weight = source.weight,
                CountRange = source.CountRange,
                PoolType = source.PoolType
            };
        }

        private static void ApplyAdvancedAdditionalApparel(
            Pawn pawn,
            KindGearData kindData,
            List<ThingDef> requiredApparel,
            List<SpecRequirementEdit> specificApparel,
            bool forceIgnore,
            TechLevel? techLevelLimit,
            float budget,
            ref float currentSpent,
            HashSet<ThingDef> protectedCoreDefs)
        {
            var extras = new List<SpecRequirementEdit>();
            if (!requiredApparel.NullOrEmpty())
            {
                extras.AddRange(requiredApparel.Select(CreateRequiredSpec));
            }
            if (!specificApparel.NullOrEmpty())
            {
                extras.AddRange(GetWhatToGive(specificApparel, pawn));
            }

            foreach (var item in extras)
            {
                if (item?.Thing == null) continue;
                if (protectedCoreDefs.Contains(item.Thing)) continue;
                if (!forceIgnore && !IsTechLevelAllowed(item.Thing, techLevelLimit)) continue;

                var created = GenerateItem(pawn, item, kindData, forceIgnore ? -1f : (budget - currentSpent));
                if (created is Apparel app)
                {
                    TryWearApparelWithBudget(pawn, app, ref currentSpent, budget, forceIgnore, protectedCoreDefs);
                }
                else
                {
                    created?.Destroy();
                }
            }
        }

        private static void ApplySimpleAdditionalApparel(
            Pawn pawn,
            KindGearData kindData,
            List<GearItem> simpleArmors,
            List<GearItem> simpleApparel,
            List<GearItem> simpleOthers,
            bool forceIgnore,
            TechLevel? techLevelLimit,
            float budget,
            ref float currentSpent,
            HashSet<ThingDef> protectedCoreDefs)
        {
            EquipSimpleApparelList(pawn, kindData, simpleArmors, forceIgnore, techLevelLimit, budget, ref currentSpent, protectedCoreDefs);
            EquipSimpleApparelList(pawn, kindData, simpleApparel, forceIgnore, techLevelLimit, budget, ref currentSpent, protectedCoreDefs);
            EquipSimpleApparelList(pawn, kindData, simpleOthers, forceIgnore, techLevelLimit, budget, ref currentSpent, protectedCoreDefs);
        }

        private static void EquipSimpleApparelList(
            Pawn pawn,
            KindGearData kindData,
            List<GearItem> gearList,
            bool forceIgnore,
            TechLevel? techLevelLimit,
            float budget,
            ref float currentSpent,
            HashSet<ThingDef> protectedCoreDefs)
        {
            if (gearList.NullOrEmpty()) return;

            var validList = gearList
                .Where(g => g != null && g.ThingDef != null)
                .Where(g => forceIgnore || IsTechLevelAllowed(g.ThingDef, techLevelLimit))
                .Where(g => protectedCoreDefs == null || !protectedCoreDefs.Contains(g.ThingDef))
                .ToList();

            if (validList.NullOrEmpty()) return;

            var workingList = validList.ToList();
            int maxAttempts = workingList.Count * 2;
            int attempts = 0;
            while (workingList.Any() && attempts < maxAttempts)
            {
                attempts++;
                var item = GetRandomGearItem(workingList);
                if (item == null) break;
                workingList.Remove(item);

                var created = GenerateSimpleItem(pawn, item.ThingDef, kindData, false, forceIgnore ? -1f : (budget - currentSpent));
                if (created is Apparel app)
                {
                    TryWearApparelWithBudget(pawn, app, ref currentSpent, budget, forceIgnore, protectedCoreDefs);
                }
                else
                {
                    created?.Destroy();
                }
            }
        }

        private static bool TryWearApparelWithBudget(
            Pawn pawn,
            Apparel app,
            ref float currentSpent,
            float budget,
            bool forceIgnore,
            HashSet<ThingDef> protectedCoreDefs)
        {
            if (app == null) return false;
            if (!app.def.destroyable || !CanWearApparel(pawn, app.def))
            {
                app.Destroy();
                return false;
            }

            if (protectedCoreDefs != null && protectedCoreDefs.Count > 0 && WouldConflictWithCoreSet(pawn, app.def, protectedCoreDefs))
            {
                app.Destroy();
                return false;
            }

            if (!forceIgnore && currentSpent + app.MarketValue > budget)
            {
                app.Destroy();
                return false;
            }

            try
            {
                pawn.apparel.Wear(app, true);
                currentSpent = pawn.apparel.WornApparel.Sum(a => a.MarketValue);
                return true;
            }
            catch (Exception ex)
            {
                Log.Warning($"[FactionGearCustomizer] Failed to wear {app} on {pawn}: {ex.Message}");
                app.Destroy();
                return false;
            }
        }

        private static bool WouldConflictWithCoreSet(Pawn pawn, ThingDef candidateDef, HashSet<ThingDef> protectedCoreDefs)
        {
            if (pawn?.apparel?.WornApparel == null || protectedCoreDefs == null || protectedCoreDefs.Count == 0)
            {
                return false;
            }

            foreach (var worn in pawn.apparel.WornApparel)
            {
                if (worn == null) continue;
                if (!protectedCoreDefs.Contains(worn.def)) continue;
                if (!AreApparelDefsCompatible(worn.def, candidateDef))
                {
                    return true;
                }
            }
            return false;
        }

        private static float GetEstimatedMarketValue(ThingDef def, ThingDef stuff, QualityCategory quality)
        {
            float baseValue = def.GetStatValueAbstract(StatDefOf.MarketValue, stuff);
            float qualityFactor = GetQualityPriceMultiplier(quality);
            return baseValue * qualityFactor;
        }

        private static float GetQualityPriceMultiplier(QualityCategory quality)
        {
            switch (quality)
            {
                case QualityCategory.Awful: return 0.2f;
                case QualityCategory.Poor: return 0.5f;
                case QualityCategory.Normal: return 1.0f;
                case QualityCategory.Good: return 1.4f;
                case QualityCategory.Excellent: return 1.8f;
                case QualityCategory.Masterwork: return 2.5f;
                case QualityCategory.Legendary: return 4.0f;
                default: return 1.0f;
            }
        }

        // Helper for Simple Mode items
        private static Thing GenerateSimpleItem(Pawn pawn, ThingDef def, KindGearData kindData, bool isWeapon, float maxPrice = -1f)
        {
            if (def == null)
            {
                Log.Error("[FactionGearCustomizer] GenerateSimpleItem called with null def.");
                return null;
            }
            if (kindData == null)
            {
                Log.Error("[FactionGearCustomizer] GenerateSimpleItem called with null kindData.");
                return null;
            }

            ThingDef stuff = def.MadeFromStuff ? GenStuff.RandomStuffFor(def) : null;
            if (def.MadeFromStuff && stuff == null)
            {
                Log.Warning($"[FactionGearCustomizer] Could not find stuff for {def.defName}. Skipping generation.");
                return null;
            }

            // Quality Logic (Pre-calculation)
            QualityCategory q = QualityCategory.Normal;
            if (isWeapon && kindData.ForcedWeaponQuality.HasValue) q = kindData.ForcedWeaponQuality.Value;
            else if (kindData.ItemQuality.HasValue) q = kindData.ItemQuality.Value;

            // Budget Check
            if (maxPrice >= 0f)
            {
                // Only check quality multiplier if the item actually has quality, otherwise it's just base * stuff
                // But GetEstimatedMarketValue applies quality multiplier anyway.
                // If the item doesn't have CompQuality, it shouldn't have quality. 
                // However, checking CompQuality requires instantiation or checking def.HasComp(typeof(CompQuality)).
                
                // If def doesn't have quality comp, we should treat quality multiplier as 1.
                if (!def.HasComp(typeof(CompQuality)))
                {
                    // If no quality, force Normal (1.0x) for calculation
                    if (GetEstimatedMarketValue(def, stuff, QualityCategory.Normal) > maxPrice) return null;
                }
                else
                {
                    if (GetEstimatedMarketValue(def, stuff, q) > maxPrice) return null;
                }
            }

            var thing = ThingMaker.MakeThing(def, stuff);

            // Quality
            CompQuality compQuality = thing.TryGetComp<CompQuality>();
            if (compQuality != null)
            {
                compQuality.SetQuality(q, ArtGenerationContext.Outsider);
            }
            
            // Color
            if (kindData.ApparelColor.HasValue && thing.TryGetComp<CompColorable>() != null)
            {
                thing.SetColor(kindData.ApparelColor.Value);
            }
            
            // Biocode (Royalty DLC)
            if (ModsConfig.RoyaltyActive && isWeapon && kindData.BiocodeWeaponChance.HasValue)
            {
                var code = thing.TryGetComp<CompBiocodable>();
                if (code != null && code.Biocodable && Rand.Chance(kindData.BiocodeWeaponChance.Value))
                {
                    code.CodeFor(pawn);
                }
            }

            return thing;
        }

        // Helper for Advanced Mode items
        private static Thing GenerateItem(Pawn pawn, SpecRequirementEdit spec, KindGearData kindData, float maxPrice = -1f)
        {
            ThingDef stuff = spec.Material;
            if (stuff == null && spec.Thing.MadeFromStuff)
            {
                 stuff = GenStuff.RandomStuffFor(spec.Thing);
            }

            // Quality Logic (Pre-calculation)
            QualityCategory q = spec.Quality ?? kindData.ItemQuality ?? QualityCategory.Normal;
            if (spec.Thing.IsWeapon && kindData.ForcedWeaponQuality.HasValue) q = kindData.ForcedWeaponQuality.Value;

            // Budget Check
            if (maxPrice >= 0f)
            {
                 if (!spec.Thing.HasComp(typeof(CompQuality)))
                 {
                     if (GetEstimatedMarketValue(spec.Thing, stuff, QualityCategory.Normal) > maxPrice) return null;
                 }
                 else
                 {
                     if (GetEstimatedMarketValue(spec.Thing, stuff, q) > maxPrice) return null;
                 }
            }

            Thing thing = ThingMaker.MakeThing(spec.Thing, stuff);
            if (thing == null) return null;

            if (ModsConfig.IdeologyActive && spec.Style != null)
            {
                thing.SetStyleDef(spec.Style);
            }

            CompQuality compQuality = thing.TryGetComp<CompQuality>();
            if (compQuality != null)
            {
                compQuality.SetQuality(q, ArtGenerationContext.Outsider);
            }

            Color color = spec.Color != default ? spec.Color : (kindData.ApparelColor ?? default);
            if (color != default)
            {
                thing.SetColor(color, false);
            }

            // Biocode (Royalty DLC)
            if (ModsConfig.RoyaltyActive)
            {
                CompBiocodable code = thing.TryGetComp<CompBiocodable>();
                if (code != null && code.Biocodable)
                {
                    if (code.Biocoded) code.UnCode();
                    
                    bool shouldBiocode = spec.Biocode;
                    if (!shouldBiocode && thing.def.IsWeapon && kindData.BiocodeWeaponChance.HasValue)
                    {
                        shouldBiocode = Rand.Chance(kindData.BiocodeWeaponChance.Value);
                    }

                    if (shouldBiocode)
                    {
                        code.CodeFor(pawn);
                    }
                }
            }

            return thing;
        }

        private static SpecRequirementEdit GeneratePoolItem(SpecRequirementEdit itemPool, Pawn pawn)
        {
            ThingDef thingDef = null;
            switch (itemPool.PoolType)
            {
                case ItemPoolType.AnyFood:
                    thingDef = GetRandomFoodItem();
                    break;
                case ItemPoolType.AnyMeal:
                    thingDef = GetRandomMealItem();
                    break;
                case ItemPoolType.AnyRawFood:
                    thingDef = GetRandomRawFoodItem();
                    break;
                case ItemPoolType.AnyMeat:
                    thingDef = GetRandomMeatItem();
                    break;
                case ItemPoolType.AnyVegetable:
                    thingDef = GetRandomVegetableItem();
                    break;
                case ItemPoolType.AnyMedicine:
                    thingDef = GetRandomMedicineItem();
                    break;
                case ItemPoolType.AnySocialDrug:
                    thingDef = GetRandomSocialDrugItem();
                    break;
                case ItemPoolType.AnyHardDrug:
                    thingDef = GetRandomHardDrugItem();
                    break;
            }

            if (thingDef == null) return null;

            // Create a new SpecRequirementEdit with the generated item
            var result = new SpecRequirementEdit
            {
                Thing = thingDef,
                SelectionMode = itemPool.SelectionMode,
                SelectionChance = itemPool.SelectionChance,
                weight = itemPool.weight,
                CountRange = itemPool.CountRange,
                PoolType = itemPool.PoolType
            };

            return result;
        }

